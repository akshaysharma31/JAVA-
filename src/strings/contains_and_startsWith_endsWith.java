package strings;

public class contains_and_startsWith_endsWith {
    public static void main(String[] args) {
        String str = "Akshay Sharma";
        // return true and false 
        System.out.println(str.contains("hay S"));
        System.out.println(str.startsWith("Aks"));
        System.out.println(str.endsWith("rma"));
    }
}
